package io.dxps.coveralls.javasample.domain.car;

public enum EngineState {

    /**
     * Engine is stopped (it is in 'parking mode').
     */
    OFF,

    /**
     * Engine is on, and wheels are running (it is in 'drive mode').
     */
    DRIVE,

    /**
     * Engine is turned on, and it is in 'neutral mode'.
     */
    ON

}
